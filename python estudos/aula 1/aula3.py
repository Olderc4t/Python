"""
Python = Linguagem de programação 
tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Hello world!')
print('Hello "world!"')

# Aspas duplas
print("Hello world!")
print("Hello 'world!'")

# Escape
print('Hello \"world!\"')

# r
print(r'Hello \"world!\"')
